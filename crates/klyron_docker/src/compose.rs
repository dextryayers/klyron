    fn detect_project_type(dir: &Path) -> String {
        if dir.join("package.json").exists() {
            if let Ok(content) = std::fs::read_to_string(dir.join("package.json")) {
                if let Ok(json) = serde_json::from_str::<serde_json::Value>(&content) {
                    if let Some(deps) = json.get("dependencies").and_then(|v| v.as_object()) {
                        if deps.contains_key("next") {
                            return "next".into();
                        }
                        if deps.contains_key("vue") || deps.contains_key("nuxt") {
                            return "vue".into();
                        }
                        if deps.contains_key("svelte") || deps.contains_key("sveltekit") {
                            return "svelte".into();
                        }
                        if deps.contains_key("@angular/core") {
                            return "angular".into();
                        }
                        if deps.contains_key("react") || deps.contains_key("react-scripts") {
                            return "react".into();
                        }
                    }
                }
            }
            return "node".into();
        }
        if dir.join("Cargo.toml").exists() {
            return "rust".into();
        }
        if dir.join("go.mod").exists() {
            return "go".into();
        }
        if dir.join("requirements.txt").exists()
            || dir.join("setup.py").exists()
            || dir.join("pyproject.toml").exists()
        {
            return "python".into();
        }
        if dir.join("composer.json").exists() {
            return "php".into();
        }
        if dir.join("index.html").exists() || dir.join("public/index.html").exists() {
            return "static".into();
        }
        "node".into()
    }
