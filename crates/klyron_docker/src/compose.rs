    pub fn generate_compose(dir: &Path) -> anyhow::Result<()> {
        let config = DockerConfig::default();
        Self::generate_compose_with_config(dir, &config)
    }

    pub fn generate_compose_with_config(dir: &Path, config: &DockerConfig) -> anyhow::Result<()> {
        let name = dir.file_name().unwrap_or_default().to_string_lossy();
        let project_type = Self::detect_project_type(dir);
        let is_prod = config.profile == DockerProfile::Prod;

        let all_services = if config.additional_services.is_empty() {
            Self::default_infra_services()
        } else {
            config.additional_services.clone()
        };

        let env_file = if is_prod { ".env.production" } else { ".env" };

        let mut compose = format!(
            r#"# docker-compose.yml generated by klyron_docker
# Profile: {profile:?}
# Project type: {project_type}

services:
  {name}:
    build:
      context: .
      dockerfile: Dockerfile
      args:
        - NODE_ENV={env}
        - BUILDKIT_INLINE_CACHE=1
    ports:
      - "{port}:{port}"
    env_file:
      - {env_file}
    environment:
      - NODE_ENV={env}
    restart: unless-stopped
    healthcheck:
      test: ["CMD", "curl", "-f", "http://localhost:{port}/health"]
      interval: 30s
      timeout: 10s
      retries: 3
      start_period: 40s
    profiles:
      - {profile:?}
    networks:
      - klyron-network

"#,
            name = name,
            port = config.port,
            env = if is_prod { "production" } else { "development" },
            profile = config.profile,
            project_type = project_type,
            env_file = env_file,
        );

        if is_prod {
            compose.push_str(&format!(
                r#"  {name}-nginx:
    image: nginx:alpine
    ports:
      - "80:80"
      - "443:443"
    volumes:
      - ./nginx.conf:/etc/nginx/nginx.conf:ro
    depends_on:
      - {name}
    profiles:
      - Prod
    networks:
      - klyron-network

"#,
                name = name,
            ));
        }

        for svc in &all_services {
            let env_vars: String = svc
                .env_vars
                .iter()
                .map(|(k, v)| format!("      {k}: \"{v}\""))
                .collect::<Vec<_>>()
                .join("\n");

            let depends: String = if svc.depends_on.is_empty() {
                String::new()
            } else {
                format!("\n    depends_on:\n      - {}\n", svc.depends_on.join("\n      - "))
            };

            let health: String = if let Some(hc) = &svc.health_check {
                format!(
                    r#"
    healthcheck:
      test: [{test}]
      interval: {interval}
      timeout: {timeout}
      retries: {retries}
      start_period: {start_period}
"#,
                    test = hc
                        .test
                        .iter()
                        .map(|t| format!("\"{t}\""))
                        .collect::<Vec<_>>()
                        .join(", "),
                    interval = hc.interval,
                    timeout = hc.timeout,
                    retries = hc.retries,
                    start_period = hc.start_period,
                )
            } else {
                String::new()
            };

            let volumes: String = if svc.volumes.is_empty() {
                String::new()
            } else {
                format!("\n    volumes:\n      - {}\n", svc.volumes.join("\n      - "))
            };

            compose.push_str(&format!(
                r#"  {svc_name}:
    image: {image}
    ports:
      - "{port}:{port}"
    environment:
{env_vars}{depends}{health}{volumes}
    restart: unless-stopped
    profiles:
      - {profile:?}
    networks:
      - klyron-network

"#,
                svc_name = svc.name,
                image = svc.image,
                port = svc.port,
                env_vars = env_vars,
                depends = depends,
                health = health,
                volumes = volumes,
                profile = svc.profile,
            ));
        }

        compose.push_str("networks:\n  klyron-network:\n    driver: bridge\n");

        let compose_path = dir.join("docker-compose.yml");
        std::fs::write(&compose_path, compose).context("Failed to write docker-compose.yml")
    }

    pub fn build(config: DockerConfig) -> anyhow::Result<()> {
        let mut cmd = std::process::Command::new("docker");
        let mut args = vec!["build".to_string()];

        if config.use_build_cache {
            args.push("--cache-from".to_string());
            args.push(config.image_name.clone());
        }

        args.push("-t".to_string());
        args.push(config.image_name.clone());
        args.push(".".to_string());

        if config.optimize {
            args.push("--squash".to_string());
        }

        for (key, val) in &config.build_args {
            args.push("--build-arg".to_string());
            args.push(format!("{key}={val}"));
        }

        cmd.args(&args);
        let status = cmd
            .current_dir(&config.project_dir)
            .status()
            .context("Failed to execute docker build")?;
        if !status.success() {
            anyhow::bail!("Docker build failed with exit code: {:?}", status.code());
        }
        Ok(())
    }

    pub fn run(config: DockerConfig) -> anyhow::Result<()> {
        let port_str = format!("{}:{}", config.port, config.port);
        let mut cmd = std::process::Command::new("docker");
        let mut args = vec![
            "run".to_string(),
            "-d".to_string(),
            "--rm".to_string(),
            "-p".to_string(),
            port_str,
        ];

        if config.profile == DockerProfile::Dev {
            args.push("-v".to_string());
            args.push(".:/app".to_string());
            args.push("-v".to_string());
            args.push("/app/node_modules".to_string());
        }

        if config.optimize {
            args.push("--restart".to_string());
            args.push("unless-stopped".to_string());
            args.push("--health-cmd".to_string());
            args.push(format!(
                "curl -f http://localhost:{}/health || exit 1",
                config.port
            ));
            args.push("--health-interval".to_string());
            args.push("30s".to_string());
        }

        args.push(config.image_name.clone());
        cmd.args(&args);

        let status = cmd
            .current_dir(&config.project_dir)
            .status()
            .context("Failed to execute docker run")?;
        if !status.success() {
            anyhow::bail!("Docker run failed with exit code: {:?}", status.code());
        }
        Ok(())
    }

    fn node_dockerfile(config: &DockerConfig, is_prod: bool) -> String {
        let cache_mount = if config.use_build_cache {
            r#"
RUN --mount=type=cache,target=/root/.npm \
    npm config set cache /root/.npm

RUN --mount=type=cache,target=/root/.npm \
    npm ci --only=production
"#
        } else {
            "\nRUN npm ci\n"
        };

        if is_prod {
            format!(
                r#"# syntax=docker/dockerfile:1.4
FROM node:20-alpine AS builder
WORKDIR /app
ARG NODE_ENV=production
ENV NODE_ENV=${{NODE_ENV}}
COPY package*.json ./{cache_mount}
COPY . .
RUN npm run build

FROM node:20-alpine AS runner
WORKDIR /app
ARG NODE_ENV=production
ENV NODE_ENV=${{NODE_ENV}}
RUN addgroup --system --gid 1001 nodejs && \
    adduser --system --uid 1001 klyron
COPY --from=builder /app/dist ./dist
COPY --from=builder /app/node_modules ./node_modules
COPY --from=builder /app/package*.json ./
RUN chown -R klyron:nodejs /app
USER klyron
EXPOSE {port}

HEALTHCHECK --interval=30s --timeout=3s --start-period=5s --retries=3 \
  CMD wget --no-verbose --tries=1 --spider http://localhost:{port}/health || exit 1

CMD ["node", "dist/index.js"]
"#,
                port = config.port,
                cache_mount = cache_mount,
            )
        } else {
            format!(
                r#"# syntax=docker/dockerfile:1.4
FROM node:20-alpine
WORKDIR /app
ARG NODE_ENV=development
ENV NODE_ENV=${{NODE_ENV}}
COPY package*.json ./
{cache_mount}
COPY . .
EXPOSE {port}

CMD ["npm", "run", "dev"]
"#,
                port = config.port,
                cache_mount = cache_mount,
            )
        }
    }

    fn rust_dockerfile(config: &DockerConfig, is_prod: bool) -> String {
        let cache_mount = if config.use_build_cache {
            r#"
RUN --mount=type=cache,target=/usr/local/cargo/registry \
    --mount=type=cache,target=/app/target \
    mkdir src && echo "fn main() {{}}" > src/main.rs

RUN --mount=type=cache,target=/usr/local/cargo/registry \
    --mount=type=cache,target=/app/target \
    cargo build --release 2>/dev/null || true
"#
        } else {
            "\nRUN mkdir src && echo \"fn main() {}\" > src/main.rs && cargo build --release 2>/dev/null || true\n"
        };

        if is_prod {
            format!(
                r#"# syntax=docker/dockerfile:1.4
FROM rust:1.78 AS builder
WORKDIR /app
COPY Cargo.toml Cargo.lock ./{cache_mount}
COPY src ./src
RUN --mount=type=cache,target=/usr/local/cargo/registry \
    --mount=type=cache,target=/app/target \
    cargo build --release

FROM gcr.io/distroless/cc-debian12
COPY --from=builder /app/target/release/app /usr/local/bin/app
EXPOSE {port}

HEALTHCHECK --interval=30s --timeout=3s --start-period=5s --retries=3 \
  CMD ["/usr/local/bin/app", "--health-check"]

CMD ["app"]
"#,
                port = config.port,
                cache_mount = cache_mount,
            )
        } else {
            format!(
                r#"# syntax=docker/dockerfile:1.4
FROM rust:1.78
WORKDIR /app
RUN cargo install cargo-watch
COPY . .
EXPOSE {port}

CMD ["cargo", "watch", "-x", "run"]
"#,
                port = config.port,
            )
        }
    }

